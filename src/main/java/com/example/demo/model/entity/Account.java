package com.example.demo.model.entity;


public class Account {
    private int Id;
    private String Name;
    
    public Account(){
        
    }
    
    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }
    
}
